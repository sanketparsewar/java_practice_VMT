package collectionsFramework;
/*
collection is a framework that is used to represent group of objects
before collection frame work  array concept can be used to represent the group of objects 
advantages:
	growable, we don't need to think about the size
	provide the data structural algorithm
	
collection is an interface located in java.util package
it is child interface of iterable interface
it is the parent interface of list, set, and Queue interface
it is considered as root interface of the collection framework 
collection interface can be used to represent a group of objects
collection interface introduced the methods like add(), addAll(), remove(), removeAll(), size(), 
isEmpty(),toArray(),contains(),containsAll(),clear(),etc.
 */